# contextlens/core/monitor.py
"""
Token Monitor — Phase 5

Responsibility: Record, aggregate, and report on token usage,
API cost, and latency across every run in a session.

Design decisions:
  - RunMetric is a lightweight dataclass (fast to create per-run)
  - All aggregation is done lazily at report time (not on every record)
  - Percentile calculation uses numpy for accuracy
  - Budget system uses configurable daily/session limits
  - JSONL store mirrors the logger pattern (Phase 2) for consistency
  - All money is stored as float USD — no currency objects needed at this scale

Production insight: In real teams, this data feeds dashboards, 
cost-allocation reports, and automated budget alerts to Slack/PagerDuty.
"""

from __future__ import annotations

import json
import statistics
from dataclasses import dataclass, field, asdict
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import numpy as np
from loguru import logger
from rich.console import Console
from rich.table import Table
from rich.panel import Panel
from rich import box

from contextlens.config import config

console = Console()


# ---------------------------------------------------------------------------
# Data model
# ---------------------------------------------------------------------------

@dataclass
class RunMetric:
    """
    Lightweight metric snapshot for one completed LensResult.

    Designed to be created immediately after a run completes.
    Stored in JSONL — one line per run — for fast append and streaming reads.
    """
    run_id: str
    timestamp: str = field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat()
    )
    provider: str = "unknown"
    model: str = "unknown"
    session_id: str = "default"

    # Token breakdown
    input_tokens: int = 0
    output_tokens: int = 0
    total_tokens: int = 0
    context_tokens: int = 0       # from ContextEntry (pre-API token count)

    # Cost (USD)
    estimated_cost_usd: float = 0.0

    # Latency
    api_latency_ms: float = 0.0   # just the API call
    wall_time_ms: float = 0.0     # end-to-end including logging + evaluation

    # Quality signal from evaluator (Phase 4)
    context_score: float | None = None
    context_grade: str | None = None

    # Tags from the original run (for filtering)
    tags: list[str] = field(default_factory=list)

    def to_dict(self) -> dict[str, Any]:
        d = asdict(self)
        d["estimated_cost_usd"] = round(d["estimated_cost_usd"], 8)
        d["api_latency_ms"] = round(d["api_latency_ms"], 2)
        d["wall_time_ms"] = round(d["wall_time_ms"], 2)
        return d


@dataclass
class ProviderStats:
    """Aggregated stats for one provider within a session."""
    provider: str
    runs: int = 0
    total_tokens: int = 0
    total_cost_usd: float = 0.0
    total_latency_ms: float = 0.0
    latencies: list[float] = field(default_factory=list)

    @property
    def avg_latency_ms(self) -> float:
        return self.total_latency_ms / max(1, self.runs)

    @property
    def avg_cost_per_run(self) -> float:
        return self.total_cost_usd / max(1, self.runs)

    @property
    def avg_tokens_per_run(self) -> float:
        return self.total_tokens / max(1, self.runs)


@dataclass
class SessionReport:
    """
    Complete monitoring report for the current session.

    Generated by TokenMonitor.get_report().
    Read by the CLI 'contextlens report' command.
    Will feed the Streamlit dashboard (Phase 10).
    """
    session_id: str
    generated_at: str
    total_runs: int
    total_tokens: int
    total_cost_usd: float
    avg_tokens_per_run: float
    avg_cost_per_run_usd: float

    # Latency percentiles (ms)
    p50_latency_ms: float
    p90_latency_ms: float
    p99_latency_ms: float
    min_latency_ms: float
    max_latency_ms: float

    # Provider breakdown
    by_provider: dict[str, ProviderStats]

    # Budget tracking
    budget_usd: float | None
    budget_used_pct: float | None
    budget_status: str          # "ok" | "warning" | "exceeded" | "no_budget"

    # Quality summary (from evaluator)
    avg_context_score: float | None
    runs_passed_evaluation: int | None

    # Top issues across session
    most_expensive_run: RunMetric | None
    slowest_run: RunMetric | None

    def to_dict(self) -> dict[str, Any]:
        d = {
            "session_id": self.session_id,
            "generated_at": self.generated_at,
            "total_runs": self.total_runs,
            "total_tokens": self.total_tokens,
            "total_cost_usd": round(self.total_cost_usd, 6),
            "avg_tokens_per_run": round(self.avg_tokens_per_run, 1),
            "avg_cost_per_run_usd": round(self.avg_cost_per_run_usd, 6),
            "latency_percentiles_ms": {
                "p50": round(self.p50_latency_ms, 1),
                "p90": round(self.p90_latency_ms, 1),
                "p99": round(self.p99_latency_ms, 1),
                "min": round(self.min_latency_ms, 1),
                "max": round(self.max_latency_ms, 1),
            },
            "by_provider": {
                name: {
                    "runs": ps.runs,
                    "total_tokens": ps.total_tokens,
                    "total_cost_usd": round(ps.total_cost_usd, 6),
                    "avg_latency_ms": round(ps.avg_latency_ms, 1),
                    "avg_cost_per_run": round(ps.avg_cost_per_run, 6),
                }
                for name, ps in self.by_provider.items()
            },
            "budget": {
                "budget_usd": self.budget_usd,
                "used_pct": round(self.budget_used_pct, 2)
                    if self.budget_used_pct is not None else None,
                "status": self.budget_status,
            },
            "quality": {
                "avg_context_score": round(self.avg_context_score, 2)
                    if self.avg_context_score is not None else None,
                "runs_passed_evaluation": self.runs_passed_evaluation,
            },
        }
        return d


